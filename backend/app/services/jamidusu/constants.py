def _compute_ziwei_table() -> dict[int, list[int]]:
    table: dict[int, list[int]] = {}
    for K in (2, 3, 4, 5, 6):
        row: list[int] = []
        for d in range(1, 31):
            quotient = (d + K - 1) // K
            remainder = quotient * K - d
            pos = (2 + quotient - 1) % 12
            if remainder % 2 == 1:
                pos = (pos - remainder) % 12
            else:
                pos = (pos + remainder) % 12
            row.append(pos)
        table[K] = row
    return table


ZIWEI_TABLE: Final[dict[int, list[int]]] = _compute_ziwei_table()


