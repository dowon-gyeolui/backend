    """24시 시각 → 12 시진 지지 인덱스 (0=子..11=亥).

    子時 = 23:00 ~ 00:59. 즉:
      23-00 → 子(0), 1-2 → 丑(1), 3-4 → 寅(2), 5-6 → 卯(3),
      7-8  → 辰(4), 9-10 → 巳(5), 11-12 → 午(6), 13-14 → 未(7),
      15-16 → 申(8), 17-18 → 酉(9), 19-20 → 戌(10), 21-22 → 亥(11).
    """
    if hh == 23:
        return 0  # 子時
    return ((hh + 1) // 2) % 12