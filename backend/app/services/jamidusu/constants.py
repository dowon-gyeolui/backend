# ── 紫微星 위치표 — 五行局 + 음력 생일 → 紫微 지지 인덱스 ──────────
# rows: 음력 생일 1..30; cols: 局數 (2,3,4,5,6) → 12지지 인덱스(0=子..11=亥)
# 표준 紫微星表 (안성표). 同 자미두수전서 起紫微訣 룰을 표로 펼친 것.

# 출처 정리:
#   水二局 d=1: 丑(1), d=2: 寅(2), d=3: 寅(2), d=4: 卯(3), d=5: 卯(3), d=6: 辰(4)...
#   각 局 마다 紫微 가 ⌈d/局⌉ 步 진행, 餘奇逆/餘偶順.
#
# 직접 산식 적용:
#   pos = (寅(2) + ⌈d/K⌉ - 1) mod 12
#   if (⌈d/K⌉*K - d) is odd: pos -= 餘  (역시계 방향)
#   else:                     pos += 餘  (시계 방향)


def _compute_ziwei_table() -> dict[int, list[int]]:
    """紫微 위치표 동적 생성. K(局數) → [day1..day30] branch_idx 리스트."""
    table: dict[int, list[int]] = {}
    for K in (2, 3, 4, 5, 6):
        row: list[int] = []
        for d in range(1, 31):
            quotient = (d + K - 1) // K  # ⌈d/K⌉
            remainder = quotient * K - d
            # 寅=2 에서 quotient 步 진행. 寅 자체가 1번째 步.
            pos = (2 + quotient - 1) % 12
            if remainder % 2 == 1:
                pos = (pos - remainder) % 12
            else:
                pos = (pos + remainder) % 12
            row.append(pos)
        table[K] = row
    return table


ZIWEI_TABLE: Final[dict[int, list[int]]] = _compute_ziwei_table()


